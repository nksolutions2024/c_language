#include <unistd.h>
#include <fcntl.h>

int main() {
    int fd = open("one_byte_each.bin", O_WRONLY | O_CREAT | O_TRUNC, 0644);
    if (fd < 0) return 1;

    for (int i = 0; i < 4096; i++) {
        // Each iteration does ONE write() system call
        char c = 'A';
        write(fd, &c, 1);
    }

    close(fd);
    return 0;
}
